// # Course Files
//
// - On the course's GitHub page you will find the course materials
//   - `Notebooks` contains the interactive notebooks
//   - `Html` contains static versions of the notebooks
//   - `Cpp` contains the notebooks as C++ files to make it easy to copy code snippets
//   - `code` contains CMake projects with code for examples and larger workshops
// - You can also download a ZIP archive with all materials from there

// %% [markdown]
//
// ### The slides
//
// - Are interactive
// - Can only be viewed and edited with the Jupyter notebook environment

// %% [markdown]
//
// ### The HTML pages
//
// - Contain exactly the same content as the interactive notebooks
// - Are static
// - But can be easily viewed in the browser

// %% [markdown]
//
// ### For each notebook there is
//
// - A video in which the actual content is discussed
// - Potentially additional workshop videos in which you will find additional workshops

// %% [markdown]
//
// ## Structure of the `Notebooks` and `Html` folders
//
// - Each of these folders contains two subfolders
//  - `Slides`
//    - Here the completed slides and workshops with solutions are included
//    - These slides correspond to the final state of the respective video
//  - `CodeAlong`
//    - Here large parts of the code are missing and the solutions to workshops are missing
//    - This is the state of the slides at the beginning of the video
//    - Use the code-alongs as a starting point for the workshops!

// %% [markdown]
//
// ## Structure of the `Code` folder
//
// - Here too there are two important subfolders
//   - `starter_kits`
//     - Project skeletons that are used, for example, to solve workshops
//   - `completed`
//     - Complete projects, for example, solutions to workshops
// - The folders are structured as CMake projects
